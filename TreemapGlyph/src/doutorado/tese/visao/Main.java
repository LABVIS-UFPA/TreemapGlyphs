    class Task extends SwingWorker<Void, Void> {

        /*
         * Main task. Executed in background thread.
         */
        @Override
        public Void doInBackground() {
            int progress = 0;
            //Initialize progress property.
            setProgress(0);
            int ordem = 0;
            while (progress < 100) {
                //Sleep for up to one second.
                try {
                    Thread.sleep(100);
                } catch (InterruptedException ignore) {
                    ignore.printStackTrace();
                }
                //Make progress.     
                ordem++;
                progress = executaTarefas(ordem, progress);
                setProgress(Math.min(progress, 100));
            }
            return null;
        }

        /*
         * Executed in event dispatching thread
         */
        @Override
        public void done() {
            Toolkit.getDefaultToolkit().beep();
            setCursor(null); //turn off the wait cursor
        }
    }

    private int executaTarefas(int ordem, int porcentagem) {
        int tarefas = 13;
        switch (ordem) {
            case 1:
                logger.info("Leitura do arquivo.");
                manipulador = new ManipuladorArquivo();
                manipulador.lerArquivo(selectedFile);
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Tratando arquivo: " + porcentagem + "%");
                break;
            case 2:
                try {
                    logger.info("Montando Objetos coluna.");
                    manipulador.montarColunas(manipulador.getCabecalho(), manipulador.getTipos());
                } catch (Exception e) {
                    logger.error("Erro montar objetos COLUNA. \n", e);
                    e.printStackTrace();
                }
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Montando colunas: " + porcentagem + "%");
                break;
            case 3:
                try {
                    logger.info("Carregando itens no treemap e define um GlyphConcreto a esse item.");
                    manipulador.carregarItensTreemap();
                } catch (Throwable e) {
                    logger.error("Erro ao criar itens no treemap. \n", e);
                    e.printStackTrace();
                }
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Carregando itens no TreeMap: " + porcentagem + "%");
                break;
            case 4:
                try {
                    logger.info("Definindo a descrio das colunas.");
                    for (int i = 0; i < manipulador.getColunas().length; i++) {
                        Coluna c = manipulador.getColunas()[i];
                        if (c != null) {
                            c.configurarDescricao(manipulador.getDadosColuna(manipulador.getCabecalho()[i]));
                        } else {
                            throw new Exception();
                        }
                    }
                } catch (Exception e) {
                    logger.error("Erro ao definir a descrio das colunas. \n", e);
                    JOptionPane.showMessageDialog(null, 
                            "There was an error reading your file. \nPlease, "
                             + "make sure your file is set up correctly \n"
                             + "and try again. \n"
                             + "-Make sure your file has headers (Column Name and Data Type, each in a row, first Column Name then Data Type) \n"
                             + "-Data types are written with the first letter uppercase and the rest lowercase (e.g. String, Double) \n"
                             + "-The accepted data types are: Integer, Double, String, Float, Boolean",
                             "There is a problem with your file !.",
                            JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
                    break;
                }
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Definindo a descrio das colunas: " + porcentagem + "%");
                break;
            case 5:
                logger.info("Preparando atributos contnuos.");
                continuousAttributes = new ArrayList<>();
                continuousAttributes = loadContinuousAttributes();
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Preparando atributos contnuos: " + porcentagem + "%");
                break;
            case 6:
                logger.info("Preparando atributos categricos.");
                categoricalAttributes = new ArrayList<>();
                categoricalAttributes = loadCategorialAttributes();
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Preparando atributos categricos: " + porcentagem + "%");
                break;
            case 7:
                logger.info("Preparando lista tamanho para o treemap.");
                loadItensTamanhoTreemap();
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Preparando lista tamanho: " + porcentagem + "%");
                break;
            case 8:
                logger.info("Preparando lista legenda para o treemap.");
                loadItensLegendaTreemap();
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Preparando lista legenda: " + porcentagem + "%");
                break;
            case 9:
                logger.info("Preparando variveis glyphs.");
                loadVariaveisGlyph(getListaAtributosCategoricos(Constantes.NivelGlyph.NIVEL_1, true), atributo1Glyph);
                loadVariaveisGlyph(loadColorAttributes2Glyphs(), atributo2Glyph);
                loadVariaveisGlyph(getListaAtributosCategoricos(Constantes.NivelGlyph.NIVEL_3, true), atributo3Glyph);
                loadVariaveisGlyph(getListaAtributosCategoricos(Constantes.NivelGlyph.NIVEL_4, true), atributo4Glyph);
//                loadVariaveisGlyph(getListaAtributosCategoricos(Constantes.NivelGlyph.NIVEL_5, true), atributo5Glyph);

                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Carregando variveis glyph: " + porcentagem + "%");
                break;
            case 10:
                logger.info("Preparando variveis hierarquia para o treemap.");
                loadItensHierarquiaTreemap(categoricalAttributes.toArray());
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Carregando variveis hierarquia Treemap: " + porcentagem + "%");
                break;
            case 11:
                logger.info("Preparando variveis Cores para o treemap.");
//                loadItensCoresTreemap(getListaAtributosCategoricos(Constantes.NivelGlyph.NIVEL_2, false));
                List<String> coresTreemap = new ArrayList<>();
                coresTreemap.add("---");
                for (String cabecalho : manipulador.getCabecalho()) {
                    coresTreemap.add(cabecalho);
                }
                loadItensCoresTreemap(coresTreemap.toArray());
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Carregando variveis cores Treemap: " + porcentagem + "%");
                break;
            case 12:
                logger.info("Preparando variveis para Detalhes sob Demanda para o treemap.");
                loadAtributosDetalhes();
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Carregando variveis para Detalhes sob Demanda: " + porcentagem + "%");
                break;
            case 13:
                logger.info("Preparando atributos para o continuous glyph.");
                String[] attributes = new String[continuousAttributes.size() - 1];
                for (int i = 0; i < continuousAttributes.size() - 1; i++) {
                    attributes [i] = continuousAttributes.get(i);
                }
                loadItensContinuouGlyph(attributes);
                porcentagem = (ordem * 100) / tarefas;
                progressoBarra.setToolTipText("Preparando atributos para o continuous glyph: " + porcentagem + "%");
                break;
            default:
                throw new AssertionError();
        }
        return porcentagem;
    }

